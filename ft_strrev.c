#include "libft.h"

void	ft_strrev(char *s, int len)
{
	char	tmp;

	if (s && len > 1)
	{
		tmp = *s;
		s[0] = s[len - 1];
		s[len - 1] = tmp;
		return (ft_strrev(++s, len - 2));
	}
}
