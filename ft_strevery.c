int	ft_strevery(char *ar, int (*f)(char c))
{
	int x;

	if (!ar || !f)
		return (0);
	x = 0;
	while (ar[x])
	{
		if (!f(ar[x]))
			return (0);
		x++;
	}
	return (1);
}
