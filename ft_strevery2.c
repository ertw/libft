int	ft_strevery2(char **ar, int (*f)(char c))
{
	int x;
	int y;

	if (!ar || ! f)
		return (0);
	x = 0;
	y = 0;
	while (ar[y])
	{
		while (ar[y][x])
		{
			if (!f(ar[y][x]))
				return (0);
			x++;
		}
		y++;
	}
	return (1);
}
